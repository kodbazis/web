<?php
    namespace Kodbazis\Generated\Post\ById;
    
    use Exception;
    use Kodbazis\Generated\Post\Error\Error;
    use Kodbazis\Generated\Post\Error\OperationError;
    use Kodbazis\Generated\Post\Post;

    
    class ByIdController
    {
        /**
         * @var ById
         */
        private $byId;
    
        /**
         * @var OperationError
         */
        private $operationError;
    
        public function __construct(ById $byId, OperationError $operationError)
        {
            $this->byId = $byId;
            $this->operationError = $operationError;
        }
    
        public function byId(string $id): Post
        {
            try {
                return $this->byId->byId($id);
            } catch (Exception $err) {
                $this->operationError->addField(Error::getOperationError());
                throw $this->operationError;
            }
        }
    }

  